   private void  combofill(){
        try{
            String sql="select * from Bus";
            pst=con.prepareStatement(sql);
            rs=pst.executeQuery();
            while(rs.next()){
                String StartingPoint=rs.getString("StartingPoint");
                jComboBox1.addItem(StartingPoint);
                 String Destination=rs.getString("Destination");
                jComboBox2.addItem(Destination);
               
            }
            
        }catch(Exception e){
                  JOptionPane.showMessageDialog(null, e);
        }
    }
